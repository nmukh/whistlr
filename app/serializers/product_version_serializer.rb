class ProductVersionSerializer < ActiveModel::Serializer
  root :product
  embed :ids, include: true
  
  attributes :id, :type, :approved, :declined, :pending, :facebook_alias, :facebook_id, :name, :twitter_alias, :website, :organization_id
  has_one :upload

  has_one :user
  has_one :master, root: :products
  has_one :version_attributes
  has_one :poll_attributes
end
